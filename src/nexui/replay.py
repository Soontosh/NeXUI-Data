def _step_report_html(task: TaskPackage, step: dict[str, Any]) -> str:
    action = step["submission"]["action"]
    explanation = html.escape(step["submission"].get("explanation", ""))
    validation_status = step["validation"]["status"]
    validation_class = "ok" if validation_status == "ok" else ("warn" if validation_status in {"blocked_by_policy", "invalid_target"} else "bad")
    notes_html = _list_html(step.get("notes", []))
    safety_html = _list_html(step.get("safety_flags", []))
    action_text = html.escape(_describe_action(task, step["before_snapshot"], action))
    before_snapshot = html.escape(step["before_snapshot"])
    after_snapshot = html.escape(step.get("after_snapshot") or "None")
    return f"""
    <article class="card step">
      <h3>Step {step["step_index"] + 1}</h3>
      <p><span class="pill">{html.escape(action["type"])}</span><strong>{action_text}</strong></p>
      <dl>
        <dt>Before snapshot</dt><dd>{before_snapshot}</dd>
        <dt>After snapshot</dt><dd>{after_snapshot}</dd>
        <dt>Validation</dt><dd class="{validation_class}">{html.escape(validation_status)}</dd>
        <dt>Explanation</dt><dd>{explanation or "None"}</dd>
        <dt>Notes</dt><dd>{notes_html}</dd>
        <dt>Safety flags</dt><dd>{safety_html}</dd>
      </dl>
    </article>
    """


def _list_html(items: list[str]) -> str:
    if not items:
        return "None"
    rendered = "".join(f"<li>{html.escape(item)}</li>" for item in items)
    return f"<ul>{rendered}</ul>"
